# 0 準備 ---------------------------------------------------------------------

# ライブラリ
import numpy as np
import matplotlib.pyplot as plt


# 1 時間発展方程式 ------------------------------------------------------------

# ＜ポイント＞
# - 時間発展方程式は｢前期の値｣に｢今期の変化量｣を加算しながら進んでいく
# - delta * dt + n[t-1]
#   --- delta(a) : 1期間の変化量（微分係数）
#   --- dt       : 変化する期間（固定）
#   --- n[t-1]   : 直前の値


# パラメータ設定
# --- 期間変化量
# --- 微分係数
dt = 1.0
a = 1.2
num = 20

# 初期化（初期値設定）
n = np.zeros(num)
n[0] = 2

# 時間発展方程式
for t in range(1, num):
    delta = a * n[t - 1]
    n[t] = delta * dt + n[t - 1]

# プロット作成
plt.plot(n)
plt.show()


# 2 パラメータの変更 ------------------------------------------------------------

# ＜ポイント＞
# - 本来のねずみの増加ペースを再現してみる
#   --- ねずみは1ヵ月で1ペア(2匹)あたり12匹を生む（a = 12/2 = 6）
#   --- 1年間を想定してnum=12とする

# 関数定義
def rat_calculation(a, num, dt=1):
    # 初期化（初期値設定）
    n = np.zeros(num)
    n[0] = 2

    # 時間発展方程式
    for t in range(1, num):
        delta = a * n[t - 1]
        n[t] = delta * dt + n[t - 1]

    # プロット作成
    plt.plot(n)
    plt.show()


# 関数実行
rat_calculation(a=1.2, num=20)
rat_calculation(a=6, num=12)
